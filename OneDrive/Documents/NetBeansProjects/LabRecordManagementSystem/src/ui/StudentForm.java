    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {

    int row = tblStudent.getSelectedRow();

    int id = Integer.parseInt(
            tblStudent.getValueAt(row, 0).toString());

    Connection con = DBConnection.getConnection();

    String sql =
            "UPDATE students SET roll_no=?,name=?,branch=?,semester=? WHERE student_id=?";

    PreparedStatement pst =
            con.prepareStatement(sql);

    pst.setString(1, txtRollNo.getText());
    pst.setString(2, txtName.getText());
    pst.setString(3, txtBranch.getText());
    pst.setString(4, txtSemester.getText());
    pst.setInt(5, id);

    pst.executeUpdate();

    JOptionPane.showMessageDialog(this,
            "Student Updated");

    loadStudents();

} catch(Exception e) {

    System.out.println(e);

}
    }//GEN-LAST:event_btnUpdateActionPerformed


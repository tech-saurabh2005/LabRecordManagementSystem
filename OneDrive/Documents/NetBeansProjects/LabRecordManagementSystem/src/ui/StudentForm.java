    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
              if(txtRollNo.getText().isEmpty() ||
       txtName.getText().isEmpty() ||
       txtBranch.getText().isEmpty() ||
       txtSemester.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this,
                "Please fill all fields");

        return;
    }

    try {

        Connection con = DBConnection.getConnection();

        String sql =
        "INSERT INTO students(roll_no,name,branch,semester) VALUES(?,?,?,?)";

        PreparedStatement pst =
                con.prepareStatement(sql);

        pst.setString(1, txtRollNo.getText());
        pst.setString(2, txtName.getText());
        pst.setString(3, txtBranch.getText());
        pst.setString(4, txtSemester.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this,
                "Student Added Successfully");

        loadStudents();

    } catch(Exception e) {

        System.out.println(e);

    }
 
    }//GEN-LAST:event_btnAddActionPerformed


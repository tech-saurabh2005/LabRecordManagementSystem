    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
    Integer.parseInt(txtMarks.getText());
}catch(Exception e){
    JOptionPane.showMessageDialog(this,
            "Marks must be number");
    return;
}
         if(cmbStatus.getSelectedIndex() == 0){

    JOptionPane.showMessageDialog(
            LabRecordForm.this,
            "Please select Status");
            return;
         }
         if(txtRollNo.getText().isEmpty() ||
   txtStudentName.getText().isEmpty() ||
   txtExperimentName.getText().isEmpty() ||
   txtMarks.getText().isEmpty()){

    JOptionPane.showMessageDialog(LabRecordForm.this,
            "Please fill all fields");
    return;
}
        try {

    Connection con = DBConnection.getConnection();

    String sql =
            "INSERT INTO lab_records(roll_no,student_name,experiment_name,submission_status,marks) VALUES(?,?,?,?,?)";

    PreparedStatement pst =
            con.prepareStatement(sql);

    pst.setString(1, txtRollNo.getText());
    pst.setString(2, txtStudentName.getText());
    pst.setString(3, txtExperimentName.getText());
    pst.setString(4,
            cmbStatus.getSelectedItem().toString());
    pst.setString(5, txtMarks.getText());

    pst.executeUpdate();

    JOptionPane.showMessageDialog(LabRecordForm.this,
            "Lab Record Added");

    loadRecords();
    btnClearActionPerformed(null);

} catch(Exception e) {

    e.printStackTrace();
JOptionPane.showMessageDialog(LabRecordForm.this, e.getMessage());

}
    }//GEN-LAST:event_btnAddActionPerformed


    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
       
    int row = tblRecords.getSelectedRow();

    if(row == -1){
        JOptionPane.showMessageDialog(LabRecordForm.this,
                "Please select a row");
        return;
    }

    try {

        int id = Integer.parseInt(
                tblRecords.getValueAt(row,0).toString());

        Connection con = DBConnection.getConnection();

        String sql =
                "UPDATE lab_records SET roll_no=?,student_name=?,experiment_name=?,submission_status=?,marks=? WHERE record_id=?";

        PreparedStatement pst =
                con.prepareStatement(sql);

        pst.setString(1, txtRollNo.getText());
        pst.setString(2, txtStudentName.getText());
        pst.setString(3, txtExperimentName.getText());
        pst.setString(4,
                cmbStatus.getSelectedItem().toString());
        pst.setString(5, txtMarks.getText());

        pst.setInt(6, id);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(LabRecordForm.this,
                "Record Updated");

        loadRecords();
        btnClearActionPerformed(null);

    } catch(Exception e) {

        e.printStackTrace();
        JOptionPane.showMessageDialog(LabRecordForm.this,
                e.getMessage());

    }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        

    int row = tblRecords.getSelectedRow();

    if(row == -1){
        JOptionPane.showMessageDialog(LabRecordForm.this,
                "Please select a row");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(LabRecordForm.
            this,
            "Are you sure to delete?",
            "Confirm",
            JOptionPane.YES_NO_OPTION);

    if(confirm != JOptionPane.YES_OPTION){
        return;
    }

    try {

        int id = Integer.parseInt(
                tblRecords.getValueAt(row,0).toString());

        Connection con = DBConnection.getConnection();

        String sql =
                "DELETE FROM lab_records WHERE record_id=?";

        PreparedStatement pst =
                con.prepareStatement(sql);

        pst.setInt(1, id);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(LabRecordForm.this,
                "Record Deleted");

        loadRecords();

    } catch(Exception e) {

        e.printStackTrace();
        JOptionPane.showMessageDialog(LabRecordForm.this,
                e.getMessage());

    }

    }//GEN-LAST:event_btnDeleteActionPerformed

